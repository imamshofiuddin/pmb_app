     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'nisn' => 'required',
        ]);

        $data = new DataSiswa();

        $data->no_peserta = "000321" . Auth::user()->id;
        $data->nisn = $request->input('nisn');
        $data->id_user = Auth::user()->id;
        $data->id_prodi = 1;

        $data->save();
        return 'Data Saved';
    }

